		System.out.println("Byte: "+byteObj);
		Short shortObj = new Short(Short.MAX_VALUE);
		System.out.println("Short: "+shortObj);
		Integer intObj = new Integer(Integer.MAX_VALUE);
		System.out.println("Integer: "+intObj);
